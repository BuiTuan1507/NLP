def remove_accents(input_str):
	s = ''
	for c in input_str:
		if c in s1:
			s += s0[s1.index(c)]
		else:
			s += c
	return s

def remove_accents_list(inp_list):
  s = list()
  for i in inp_list:
    s.append(remove_accents(i))
  return s

def readData(fileName):
    data = []
    file = open(fileName, "r",encoding="utf8")

    for word in file.read().split():
        data.append(word)

    file.close()
    return data

